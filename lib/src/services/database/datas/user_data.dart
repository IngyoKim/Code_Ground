class UserData {
  final String userId;
  final String name;
  final String email;
  final String profileImageUrl;
  final String nickname;
  final bool isAdmin;

  UserData({
    required this.userId,
    required this.name,
    required this.email,
    required this.profileImageUrl,
    required this.nickname,
    required this.isAdmin,
  });
}
